#variável do tipo inteiro
preco = 10
print(preco)

#podemos converter o tipo dela dessa forma
preco = float(preco)
print(preco)

preco = 10 / 2
print(preco)


preco = 10.30
preco = int(preco)
print(preco)

#convertendo para string
preco = 10.50
idade = 226

print(str(preco))
print(str(idade))

texto = f"idade {idade} preco {preco}"
print(texto)

#convertendo string para número
preco = "10.50"
idade = "26"

print(float(preco))
print(int(idade))
